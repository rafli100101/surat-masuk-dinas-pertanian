                            <p align="justify">Dinas pertanian merupakan unsur pelaksana pemerintah daerah, yang dipimpin oeh kepala dinas yang berada dibawah dan bertanggung jawab kepada Bupati melalui sekretariat kabupaten. Dinas Pertanian Kabupaten Asahan diresmikan pada tahun 1970-an oleh kepala daerah tingkat II Kabupaten Asahan. Pada umumnya kantor Dinas Pertanian tanaman pangan diberi nama Dinas Pertanian untuk tingkat II Kabupaten Asahan. Selanjutnya berturut-turut yang memimpin Dinas Pertanian Kabupaten Asahan yaitu sebagai berikut:</p>


    <ol>
<li>Ir. Muis Nasution memimpin sejak tahun 1978 sampai tahun 1987</li>
<li>Ir. Mansyur Pane memimpin mulai tahun 1988 sampai tahun 1990</li>
<li>Ir. Nurkan Nasution memimpin mulai tahun 1991 sampai tahun 2000</li>
<li>Amir Hakim, SP memimpin mulai tahun 2001 sampai tahun 2004</li>
<li>Ir. Oktoni Eryanto, M.MA memimpin mulai tahun 2005 sampai tahun 2007</li>
<li>Ir. Jahela Nasution memimpin mulai tahun 2008 sampai tahun 2011</li>
<li>Ir. Oktoni Eryanto, M.MA memimpin mulai tahun 2012 sampai tahun 2017</li>
<li>Ir. Hazairin, MM memimpin mulai tahun 2017 sampai tahun 2018.</li>
<li>Ir. Oktoni Eryanto, M.MA memimpin sejak tahun 2018 sampai dengan sekarang.</li>
    </ol>


<p align="justify"> Pada umumnya tahun 1980 masa kepemimpinan Bapak Ir Mansyur Pane, Dinas pertanian kabupaten tingkat II mengalami perubahan nama menjadi Dinas Pertanian dan Peternakan tingkat II Kecamatan Kisaran Timur. Pada tahun 2001 mengalami perubahan nama kembali menjadi Dinas Pertanian dan Peternakan Kecamatan Kisaran Timur. Sejak tahun 2008 telah ditetapkan kembali nama instansi menjadi Dinas Pertanian Kabupaten Asahan. Sejalan uraian diatas dan berdasarkan Surat Keputusan Gubernur Sumatera Utara tanggal 8 februari 1978 Nomor 8 mengenai instruksi menyusun organisasi dan tata kerja pada Dinas Pertanian Tingkat II Kabupaten Asahan dengan suatu peraturan yang dibentuk berdasarkan kepada:</p>

<ol>
<li>Undang-undang No. 5 Tahun 1974 tentang pokok Pemerintahan Daerah.</li>
<li>Surat keputusan menteri dalam negeri No. 3 Tahun 1978 tentang pedoman susuran organisasi tata kerja Dinas Pertanian.</li>
<li>Undang-undang No.7 Tahun 1976 tentang pembentukan daerah otonomi Kabupaten Asahan di dalam lingkungan daerah provinsi Sumatera Utara. Dengan persetujuan DPRD Kabupaten Asahan daerah, pada sidang plena bertepatan pada tanggal 17 September 1979 hari rabu sesuai dengan pasal 8 mengenai peningkatan dan cara kerja pimpinan dinyatakan sebagai berikut:
a. Kepala Dinas diangkat dan diberhentikan oleh Kepala Daerah.
b. Kepala Dinas tidak boleh memangku jabatan rangkap kecuali dengan persetujuan Kepala Daerah.</li>
</ol>

<p align="justify">Dengan keluarnya keputusan Bupati kepala daerah dtentukan dan Dimaksudkan dalam peraturan Daerah sebagai berikut : "Tujuan Dinas Pertanian didirikan adalah untuk mengembangkan dan meningkatkan Padi, palawijanya dan
sayuran". Selain itu dinas pertanian juga membahas masalah pertanian agar memberi penyuluhan kepada petani dalam rangka menuju pelestarian swasembada padi, sehingga terciptanya agrobisnis dan produktivitas para petani yang lebih baik demi memenuhi kebutuhan bahan pangan.</p>
